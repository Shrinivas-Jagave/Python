def Arithmetic_Operators():
    a = 10
    b = 20
    print("1. Addition Operator")
    print(f"a + b = {a + b}\n")

    print("2. Subtraction Operator")
    print(f"a - b = {a - b}\n")

    print("3. Multiplication Operator")
    print(f"a * b = {a * b}\n")

    print("4. Division Operator")
    print(f"a / b = {a / b}\n")

    print("5. Floor Division Operator")
    print(f"a // b = {a // b}\n")

    print("6. Modulus Operator")
    print(f"a % b = {a % b}\n")

    print("5. Exponential Operator")
    print(f"a ** b = {a ** b}\n")


def main():
    Arithmetic_Operators()

main()
